#include "libft.h"

char	ft_strcat(char *s1, const char *s2)
{
	char	*s;

	while (*s)
		s++;
	while (*s2)
	{
		*s = *s2;
		s++;
		s2++;
	}
	*s1 = "\0";
	return (s1);
}
